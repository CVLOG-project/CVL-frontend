const Content = ({ data }: { data: string }) => {
  const checkLanguage = (arr: string[], val: string) => {
    return arr.some((arrVal: string) => val === arrVal) ? val : '';
  };

  return (
    <div className="w-[330px] md:w-[535px]  xl:w-[720px] xl:px-4 min-h-[200px]">
      <ReactMarkdown
        className="contentMarkdown "
        rehypePlugins={[rehypeRaw]}
        remarkPlugins={[remarkGfm]}
        components={{
          code({ inline, className, children, ...props }) {
            const match = /language-(\w+)/.exec(className || '');
            return !inline && match ? (
              <CopyBlock
                language={checkLanguage(languageArr, match[1])}
                text={String(children).replace(/\n$/, '')}
                theme={dracula}
                showLineNumbers={true}
                wrapLines={true}
                codeBlock
              />
            ) : (
              <code
                className={className}
                style={{
                  color: '#eb5757',
                  padding: '2px 4px',
                }}
                {...props}
              >
                {children}
              </code>
            );
          },
        }}
      >
        {data}
      </ReactMarkdown>
    </div>
  );
};

export default Content;
